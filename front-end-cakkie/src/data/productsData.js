// Random comment generator for each status
function generateRandomComments(status, numberOfComments) {
  const comments = [];
  const templates = commentTemplates[status];
  const shuffledUsernames = shuffleUsernames();

  for (let i = 0; i < numberOfComments; i++) {
    const randomIndex = Math.floor(Math.random() * templates.length);
    const randomUsernameIndex = i % shuffledUsernames.length;
    const randomComment = templates[randomIndex];
    const quantity = Math.floor(Math.random() * 3) + 1; // Random quantity

    comments.push({
      username: shuffledUsernames[randomUsernameIndex],
      quantity: quantity,
      comment: `${randomComment.flavor}, ${randomComment.quality}, ${randomComment.packaging}`,
      flavor: randomComment.flavor,
      quality: randomComment.quality,
      packaging: randomComment.packaging
    });
  }

  return comments;
}

